<p>
    The <code>/gender</code> command lets you change the gender of your player character. This can be done at any time as the change
    is cosmetic only. Male and female player characters have equal statistics, which are influenced only by race and
    profession (which cannot be changed without generating a completely new character and deleting the old one).
</p>
