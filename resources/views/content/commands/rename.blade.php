<p>
    This command will allow you to rename one of your weapons or armour pieces to any new name up to 20 characters in
    length. This name is then shown in your <code>/profile</code>, and when attacking another player in PvP.
    You require <a href="https://premium.ssod.org">Seven Spells Premium</a> to use this command.
</p>
